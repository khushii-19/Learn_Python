# -----------------------------
# WHILE LOOP: Print 1 to 50
# -----------------------------

print("WHILE LOOP: Printing numbers from 1 to 50")
i = 1
while i <= 50:
    print(i)
    i += 1
 

# -----------------------------
# WHILE LOOP: Print contents of a list
# -----------------------------

print("WHILE LOOP: Printing list elements")
fruits = ["apple", "banana", "mango"]
index = 0
while index < len(fruits):
    print(fruits[index])
    index += 1

